package com.trash.pojo;

public class Rubish {
    private Integer id;
    private String rtype;
    private String whd;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getRtype() {
        return rtype;
    }

    public void setRtype(String rtype) {
        this.rtype = rtype;
    }

    public String getWhd() {
        return whd;
    }

    public void setWhd(String whd) {
        this.whd = whd;
    }
}
